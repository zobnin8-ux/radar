import { escapeTelegramHtml } from "../utils/telegramHtml.js";

/** Текст анонса рубрики — только в первом посте серии */
export function buildGitTrendIntroHtml(): string {
  return [
    `🔮 <b>Радар будущего · GitHub-сигналы</b>`,
    ``,
    `Запускаем еженедельную рубрику на основе мониторинга GitHub.`,
    ``,
    `<b>Что это:</b>`,
    `• отслеживаем, какие категории проектов растут за неделю`,
    `• берём только устойчивые тренды — когда движение идёт сразу в нескольких репозиториях, а не в одном хайповом`,
    `• публикуем 0–3 сигнала — не каждую неделю обязательно есть повод`,
    ``,
    `<b>Что это НЕ:</b>`,
    `• не рейтинг «топ-10 по звёздам»`,
    `• не реклама конкретного репо`,
    `• не новости из RSS (для этого есть «Направление недели»)`,
    ``,
    `<b>Что вы увидите в посте:</b>`,
    `📈 факт с GitHub — что растёт и почему`,
    `🔭 гипотеза Радара — почему это может иметь значение`,
    `🔗 ссылки на репозитории-доказательства`,
    ``,
    `Расписание: воскресенье, после рубрики «Направление недели».`,
    ``,
    `${escapeTelegramHtml("GitHub показывает, что кодят сегодня.")}`,
    `${escapeTelegramHtml("Радар будущего — зачем это может быть важно завтра.")}`,
    ``,
    `━━━━━━━━━━━━━━━━`,
  ].join("\n");
}
